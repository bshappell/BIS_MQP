# Stage Class


class Stage(object):

	def __init__(self, numberBlades, filletRadius, distBetweenBlades, largeBBRadius, smallBBRadius):

		self.numberBlades = numberBlades
		self.filletRadius = filletRadius
		self.distBetweenBlades = distBetweenBlades
		self.largeBBRadius = largeBBRadius
		self.smallBBRadius = smallBBRadius

